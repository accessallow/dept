    private void loginBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBTNActionPerformed
        // TODO add your handling code here:

        String username = user_field.getText().trim();
        String pass = new String(pass_field.getPassword());

        String db_user = null, db_pass = null;

        try {

            Class.forName("com.mysql.jdbc.Driver");

            Connection conn = DriverManager.getConnection(db_url, uid, password);
            Statement st = conn.createStatement();

            ResultSet rs = st.executeQuery("select * from accounts where username='" + username + "' and password='" + pass + "';");

            while (rs.next()) {

                db_user = (String.valueOf(rs.getObject("username")));
                db_pass = (String.valueOf(rs.getObject("password")));

            }

        } catch (ClassNotFoundException | SQLException ex) {
            System.out.println(ex);
            System.out.println(ex.getMessage());
            System.out.println(ex.getClass().toString());
        }

        if (db_user.compareTo(username) == 1 && db_pass.compareTo(pass) == 1) {
            JOptionPane.showMessageDialog(login_dialog, "Credits Matched.Access Granted", "Welcome", JOptionPane.INFORMATION_MESSAGE);
            login = 1;
            login_dialog.dispose();
            password = "admin";
        } else {
            JOptionPane.showMessageDialog(login_dialog, "Access Denied", "Login Fail", JOptionPane.ERROR_MESSAGE);
            System.exit(0);
        }

    }//GEN-LAST:event_loginBTNActionPerformed

